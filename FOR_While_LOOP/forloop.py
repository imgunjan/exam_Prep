"""
for loop:
The for loop is used when you know the number of iterations (how many times the loop should run) beforehand.
It is commonly used to iterate over a sequence (such as a list, tuple, or string).
The loop variable is assigned a new value from the sequence on each iteration.
"""

fruits = ["Apple", "Mango", "Orange", "Grapes", "Banana"]
for fruit in fruits:
    print(fruit)

"""
Output
Apple
Mango
Orange
Grapes
Banana
"""
