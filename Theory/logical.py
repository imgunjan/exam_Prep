# Arithmetic, logical and comparison operators with examples

# Arithemetic operation are basically mathmatics operations * - + /
print(10 + 20)
print(9 - 6)
print(9 * 3)

# Loical operator are used to combine or neglate the conditional Statement And , Or , Not
print(True and True)
print(True and False)
print(True or False)
print(True or False)
print(not True)
print(not False)

# Comparison Operator are used to compare the value between and return boolean

print(10 <= 3)
print(10 == 10)
print(10 > 3)
print(10 != 3)
print(10 >= 3)
